<?php

namespace Tiix\Calculator;

/**
 * Class Calculator
 *
 * @package Tiix\Calculator
 */
class Calculator
{
    /**
     * @param $x
     * @param $y
     * @return mixed
     */
    public function add($x, $y)
    {
        return $x + $y;
    }

    /**
     * @param $x
     * @param $y
     * @return mixed
     */
    public function substract($x, $y)
    {
        return $x - $y;
    }
}
